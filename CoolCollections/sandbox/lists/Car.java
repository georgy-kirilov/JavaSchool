package lists;

public class Car
{
	public String code;
	public Integer kilometers;
	public int seats;
	public boolean isOld;
	
	public Car(String code, Integer kilometers, int seats, boolean isOld)
	{
		this.code = code;
		this.kilometers = kilometers;
		this.seats = seats;
		this.isOld = isOld;
	}
}
